char	*ft_strupcase(char *str)
{
	int itr;

	itr = 0;
	while (str[itr] != '\0')
	{
		if (str[itr] >= 'a' && str[itr] <= 'z')
		{
			str[itr] = str[itr] - 32;
		}
		itr++;
	}
	return (str);
}
