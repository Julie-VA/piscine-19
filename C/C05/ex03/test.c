int		ft_recursive_power(int nb, int power)
{
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	if (power == 1)
		return (nb);
	return (nb * ft_recursive_power(nb, power - 1));
}

#include <stdio.h>

int main()
{
	int i = 0;

	i = ft_recursive_power(-4, 3);
	printf("%d", i);
}
