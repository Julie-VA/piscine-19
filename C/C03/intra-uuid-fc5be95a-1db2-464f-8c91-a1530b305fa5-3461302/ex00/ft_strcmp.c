int		ft_strcmp(char *s1, char *s2)
{
	int itr;

	itr = 0;
	while (s1[itr] == s2[itr] && s1[itr] != '\0' && s2[itr] != '\0')
		itr++;
	if (s1[itr] != s2[itr])
		return (s1[itr] - s2[itr]);
	else
		return (0);
}
